/////////////////////////////////////////////////
// Import Dependencies
//////////////////////////////////////////////////
const express = require("express");
require("dotenv").config(); // Load ENV
const {signupR, loginR} = require("../routes/user")

////////////////////////////////////////////
// Create Router
////////////////////////////////////////////
const router = express.Router();

/////////////////////////////////////////////
// Routes
//////////////////////////////////////////////

router.post("/login", loginR);

router.post("/signup", signupR);

//////////////////////////////////////////
// Export the Router
//////////////////////////////////////////
module.exports = router;
